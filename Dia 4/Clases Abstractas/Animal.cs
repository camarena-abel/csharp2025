// Las clases abstractas pueden tener metodos abstractos
// Las clases que heredan una clase abstracta deben implementar los metodos abstractos!!!
// No podemos crear objetos de la clase abstracta directamente
public abstract class Animal
{
    public abstract void Dormir(); // SI ES ABSTRACTO, NO PUEDE TENER IMPLEMENTACION

    public abstract void Comer();  // SI ES ABSTRACTO, NO PUEDE TENER IMPLEMENTACION
}